async function ensureDefaultBlogCategories(prisma) {
  for (const [name, description] of letsdoCategoryDefinitions) {
    await prisma.blogCategory.upsert({
      where: { slug: slugify(name) },
      update: { name, description },
      create: { name, slug: slugify(name), description },
    });
  }
}

async function ensureDefaultBlogTags(prisma) {
  for (const tag of letsdoTagDefinitions) {
    const name = tag.replace(/-/g, " ");
    await prisma.blogTag.upsert({
      where: { slug: slugify(tag) },
      update: { name },
      create: { name, slug: slugify(tag) },
    });
  }
}

